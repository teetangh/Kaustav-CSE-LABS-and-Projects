	AREA RESET , DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10000000
	DCD Reset_Handler
	AREA MYCODE, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=N1
	LDR R1,[R0]
	MOV R2,#0
	MOV R3,#1
	LDR R4,=DST
LOOP
	ADD R5,R2,R3
	MOV R2,R3
	MOV R3,R5
	STR R5,[R4],#4
	SUBS R1,#1
	BNE LOOP
STOP
	B STOP
N1 DCD 8
	AREA MYDATA, DATA, READWRITE
DST DCD 0
	END