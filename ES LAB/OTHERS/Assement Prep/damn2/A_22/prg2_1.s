	AREA RESET , DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10000000
	DCD Reset_Handler
	AREA MYCODE, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=SRC
	LDR R1,=DST
	MOV R3,#10
LOOP
	LDR R4,[R0]
	MOV R2,#0
	ADDS R2, R4
	ADC R2,#0
	ADD R0,#4
	SUBS R3,#1
	BNE LOOP
	STR R2,[R1]
STOP
	B STOP
SRC DCD 0X1,0X2,0X3,0X4,0X5,0X6,0X7,0X8,0X9,0XA
	AREA MYDATA, DATA, READWRITE
DST DCD 0
	END