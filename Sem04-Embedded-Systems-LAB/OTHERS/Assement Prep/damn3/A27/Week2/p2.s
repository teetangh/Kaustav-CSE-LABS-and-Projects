	AREA ad,CODE,READONLY
	EXPORT Reset_Handler
	ENTRY
Reset_Handler
	LDR R0,=FIR
	LDR R1,=SEC
	LDR R6,=RES
	MOV R2,#3
	LDR R3,[R0]
	LDR R4,[R1]
	MOV R5,#0
	MOV R7,#0
	ADDS R5,R4,R3
	ADC R7,R7,#0
	ADD R0,R0,#0x00000004
	ADD R1,R1,#0x00000004
	STR R5,[R6]
	ADD R6,R6,#0x00000004
LOOP
	MOV R5,#0
	LDR R3,[R0]
	LDR R4,[R1]
	ADDS R5,R4,R3
	ADC R7,R7,#0
	ADDS R5,R7
	MOV R7,#0
	ADC R7,R7,#0
	ADD R0,R0,#0x00000004
	ADD R1,R1,#0x00000004
	STR R5,[R6]
	ADD R6,R6,#0x00000004
	SUBS R2,R2,#1
	BNE LOOP
STOP
	B STOP
FIR	DCD 1,3,5,4
SEC	DCD 6,8,4,3
	AREA mydata,DATA,READWRITE
RES	DCD 0,0,0,0
	END