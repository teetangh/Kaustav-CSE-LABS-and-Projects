	AREA RESET , DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10000000
	DCD Reset_Handler
	AREA MYCODE, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=N1
	LDR R1,=N2
	LDR R4,=DST
	LDR R5,[R0]
	LDR R6,[R1]
	SUBS R3,R5,R6
	STR R3,[R4]
STOP
	B STOP
N2 DCD 2
N1 DCD 59
	AREA MYDATA, DATA, READWRITE
DST DCD 0
	END