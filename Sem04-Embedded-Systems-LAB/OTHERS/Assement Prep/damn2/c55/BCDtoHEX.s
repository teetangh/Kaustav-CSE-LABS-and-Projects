AREA RESET, DATA, READONLY
		EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler

	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R8,=DST
	LDR R10,=NUM
	LDR R0, [R10]
	MOV R4, #1
	MOV R6, #0
	MOV R7, #0X0A
UP2	AND R1, R0, #0X0F	
	MLA R6, R4, R1, R6
	MUL R4, R4, R7
	MOVS R0, R0, LSR #4
	BNE UP2
	STR R6, [R8]
STOP
	B STOP
NUM DCD 0X1299
	AREA mydata, DATA, READWRITE
DST DCD 0
	END