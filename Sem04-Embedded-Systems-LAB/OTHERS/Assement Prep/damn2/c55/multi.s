	AREA RESET, DATA, READONLY 
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =val1
	LDR R1, =val2
	LDR R2,[R0]
	LDR R3,[R1]
	LDR R4,=DST
	LDR R5,=D
	UMULL R5,R4,R3,R2
STOP
	B STOP
val1 DCD 6
val2 DCD 2
	AREA mydata, DATA, READWRITE
DST DCD 0
D DCD 0
	END