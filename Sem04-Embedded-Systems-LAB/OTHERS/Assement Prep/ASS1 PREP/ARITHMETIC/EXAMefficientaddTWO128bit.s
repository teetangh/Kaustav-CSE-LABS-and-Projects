	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	ALIGN
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R1,=VAL1
	LDR R2,=VAL2
	LDR R3,=DST
	
	MOV R4,#0
	MOV R7,#4
	MOV R9,#0
	
	
LOOP LDR R5,[R1]
	LDR R6,[R2]
	
	MSR XPSR,R9
	ADCS R8,R5,R6
	STR R8,[R3],#4
	ADD R1,#4
	ADD R2,#4
	SUBS R7,#1
	BNE LOOP
	
STOP
	B STOP
	ALIGN
VAL1 DCD 1,2,3,4
VAL2 DCD 4,3,2,1
	AREA mydata,DATA,READWRITE
DST DCD 0
	END