	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR   R0,=0X01020304
	MOV R1,#0X0000000F
	MOV R2,#4
	MOV R3,#0
	LDR R5,=RES
UP AND R4,R0,R1
   LSR R0,#4
   LSL R1,#4
   ORR R3,R4
   SUBS R2,#1
   BNE UP
STOP
    B STOP
	AREA MYDATA, DATA, READWRITE
RES DCD 0
	END