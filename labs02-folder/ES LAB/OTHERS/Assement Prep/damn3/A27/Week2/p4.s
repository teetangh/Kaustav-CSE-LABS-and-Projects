	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	AREA reverse,CODE,READONLY
	EXPORT Reset_Handler
	ENTRY
Reset_Handler
	LDR R0,=FIR
	LDR R1,=SEC
	LDR R3,=RES
	MOV R2,#4
	MOV R4,#0xFFFFFFFF
	MSR XPSR,R4
	MOV R8,#0
LOOP
	LDR R5,[R0],#4
	LDR R6,[R1],#4
	SBCS R8,R6,R5
	STR R8,[R3],#4
	SUBS R2,#1
	BNE LOOP
STOP
	B STOP
FIR	DCD 1,3,5,4
SEC	DCD 6,8,4,3
	AREA mydata,DATA,READWRITE
RES	DCD 0,0,0,0
	END