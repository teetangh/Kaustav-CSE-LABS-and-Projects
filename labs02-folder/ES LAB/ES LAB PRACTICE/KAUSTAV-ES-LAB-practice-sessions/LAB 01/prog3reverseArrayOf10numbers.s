	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode , CODE , READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=DST
	MOV R1,#1
	MOV R2,#10
LOOP
	STR R1,[R0]
	ADD R1,#1
	ADD R0,#4
	SUBS R2,#1
	BNE LOOP
STOP
	B STOP
	ALIGN
	AREA mydata , DATA , READWRITE
DST DCD 0
	END